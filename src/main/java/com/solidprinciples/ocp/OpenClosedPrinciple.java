package com.solidprinciples.ocp;

/*
O in SOLID stands for OPEN/CLOSE principle (OCP)
 */
public class OpenClosedPrinciple {
/*
Software entities (such as classes, modules, and functions)
 should be open for extension but closed for modification.

This means that you should be able to add new functionality to a class or
module without altering its existing code. Instead of modifying the existing code,
you should achieve this by extending it.
     */

    /*
    How to attain OCP?
    Use abstraction (abstract classes/ interfaces) to implement common functionality
    Create concrete implementation of these abstractions and
    when adding new functionalities introduce a new class that implements the existing class/interface
    rather than modifying existing classes.
     */
}
